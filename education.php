<h4>What Can I Do?</h4>
<p>The planet may have to feed a global population of 10 billion people by 2050. With that milestone looming, organizations like the U.N., the Natural Resources Defense Council, and Food Tank are working on ways to make food systems more efficient.</p>

<h5> Distribution </h5>
<p>
    <b>Problem:</b> If a supermarket rejects food once it leaves a processing plant—say, for having too many tomatoes already—the truck driver may not be able to find another buyer before the food spoils.
    <br><b>Solution:</b> New mobile-phone apps, including one called Food Cowboy, help drivers locate nearby food banks that might take the shipment.
    </p>

<h5> Consumption </h5>

<p>
    <b>Problem:</b> At the last stage, consumers in industrialized countries waste five times as much food as those in developing ones. In the U.S., that means 35 million tons of food each year head to landfills and incinerators.
    <br><b>Solution:</b>  In the U.K., a public-awareness campaign cut household losses by 20 percent, by encouraging actions such as taking more frequent shopping trips to prevent groceries from spoiling.
</p>


